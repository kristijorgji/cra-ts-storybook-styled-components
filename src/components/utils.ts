export function atomStory(name: string): string {
    return `atoms/${name}`;
}

export function moleculeStory(name: string): string {
    return `molecules/${name}`;
}

export function organismsStory(name: string): string {
    return `organisms/${name}`;
}

export function pagesStory(name: string): string {
    return `pages/${name}`;
}
